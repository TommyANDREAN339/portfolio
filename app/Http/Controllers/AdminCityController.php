     */
    public function store(Request $request)
    {
        $validateData = $request->validate([
             'name' => 'required|min:5|max:255',
             'slug' => 'required|unique:cities'
        ]);

        City::create($validateData);

        return redirect('/dashboard/cities')->with('create success');
